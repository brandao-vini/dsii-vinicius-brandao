# --- Programa 2: Antecessor e Sucessor ---

print("\n--- Descubra o Antecessor e o Sucessor ---")

# Pede um número e o converte para inteiro com a função int()
numero = int(input("Digite um número inteiro: "))

# Calcula o antecessor (número - 1)
antecessor = numero - 1

# Calcula o sucessor (número + 1)
sucessor = numero + 1

# Mostra os resultados na tela
print(f"O antecessor de {numero} é {antecessor}.")
print(f"O sucessor de {numero} é {sucessor}.")